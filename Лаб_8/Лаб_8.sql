SELECT * from Клієнт
ORDER BY Місто,Вулиця,Будинок ASC; 

SELECT СТО.Назва,СТО.Сайт,СТО.Рейтинг,Клієнт.Прізвище,Клієнт.[Ім’я],Замовлення.[Дата замовлення]
from (СТО INNER JOIN Замовлення ON Замовлення.[Код СТО]=СТО.[Код СТО])
INNER JOIN Клієнт ON Клієнт.[Код клієнта]=Замовлення.[Код клієнта]
WHERE YEAR( [Дата замовлення])=2019
ORDER BY Рейтинг DESC; 

SELECT  Місто, COUNT([Код СТО]) as Кількість from СТО
GROUP BY СТО.Місто;